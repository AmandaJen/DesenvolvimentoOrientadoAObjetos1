package ExerciciosOrientacaoAObjetos.Casa;

public class C01ex02 {
    public static void main(String[] args) {
        double media1;
        double media2;
        double somamedias;
        double mediafinal;
        media1= (9+8+7)/3;
        media2= (4+5+6)/3;
        somamedias = media1 + media2;
        mediafinal = somamedias/2;
        System.out.println("Valor media 1: "+media1);
        System.out.println("Valor media 2: "+media2);
        System.out.println("Soma das medias: "+somamedias);
        System.out.println("Valor da media final: "+mediafinal);
    }
}
